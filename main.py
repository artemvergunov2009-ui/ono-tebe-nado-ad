MODELS_TO_TRY = [
    'gemini-3.1-pro-preview',
    'gemini-3-flash-preview',
    'gemini-3.1-flash-lite',
    'gemini-2.5-pro',
    'gemini-2.5-flash',
    'gemini-2.5-flash-lite',
    'gemini-2.0-flash',
    'gemini-2.0-flash-lite',
    'gemini-1.5-pro',
    'gemini-1.5-flash'
]

print("Подключение к серверам ВКонтакте...")
vk_session = vk_api.VkApi(token=VK_TOKEN)
longpoll = VkBotLongPoll(vk_session, VK_GROUP_ID)
vk = vk_session.get_api()

def get_main_keyboard():
    keyboard = VkKeyboard(one_time=False)
    keyboard.add_button('Статус', color=VkKeyboardColor.POSITIVE)
    keyboard.add_button('Помощь', color=VkKeyboardColor.PRIMARY)
    return keyboard.get_keyboard()

def send_message(peer_id, text, keyboard=None):
    params = {
        'peer_id': peer_id,
        'message': text,
        'random_id': get_random_id() 
    }
    if keyboard:
        params['keyboard'] = keyboard
    vk.messages.send(**params)

print("✅ Администрация Свахуильска (ВК) успешно запущена! Жду обращений...")

for event in longpoll.listen():
    if event.type == VkBotEventType.MESSAGE_NEW:
        msg = event.obj.message
        text = msg.get('text', '')
        peer_id = msg['peer_id']
        from_id = msg['from_id']
        
        if not text:
            continue

        text_lower = text.lower().strip()

        if text_lower == 'статус':
            send_message(peer_id, "✅ Администрация на связи и готова к работе!", get_main_keyboard())
            continue
        elif text_lower == 'помощь':
            send_message(peer_id, "Для обращения к Администрации начните сообщение со слов 'Ким' или 'Администрация'. Вы также можете попросить нас выпустить официальный указ или документ.", get_main_keyboard())
            continue

        is_private = peer_id == from_id
        bot_id_in_chat = -int(VK_GROUP_ID)
        is_reply = False
        
        if 'reply_message' in msg and msg['reply_message']['from_id'] == bot_id_in_chat:
            is_reply = True
            
        starts_with_kim = text_lower.startswith("ким")
        starts_with_admin = text_lower.startswith("администрация")

        if is_private or is_reply or starts_with_kim or starts_with_admin:
            user_query = text
            if starts_with_kim:
                user_query = text[3:].strip(" .,?!")
            elif starts_with_admin:
                user_query = text[13:].strip(" .,?!")
                
            if not user_query:
                send_message(peer_id, "Слушаю вас. Сформулируйте ваш запрос к Администрации.", get_main_keyboard())
                continue

            vk.messages.setActivity(type='typing', peer_id=peer_id)
            response_text = None
            
            print(f"\n--- Поступило обращение: '{user_query}' ---")
            
            for idx, api_key in enumerate(GEMINI_API_KEYS, 1):
                client = genai.Client(api_key=api_key)
                print(f"[🔄] Пробуем ТОКЕН №{idx}...") 
                
                for model_name in MODELS_TO_TRY:
                    try:
                        print(f"  -> Модель {model_name}...")
                        response = client.models.generate_content(
                            model=model_name,
                            contents=user_query,
                            config=types.GenerateContentConfig(
                                system_instruction=system_prompt,
                                max_output_tokens=8192,
                                temperature=0.6 
                            )
                        )
                        response_text = response.text
                        print(f"  [✅] Ответ успешно сгенерирован!")
                        break
                    except Exception as e:
                        print(f"  [❌] Ошибка: {e}") 
                        continue
                
                if response_text:
                    break
                    
            if response_text:
                send_message(peer_id, response_text, get_main_keyboard())
            else:
                send_message(peer_id, "❌ Канцелярия Администрации сейчас перегружена запросами. Пожалуйста, обратитесь позже.", get_main_keyboard())
